USE WebStor;
/*
Задача 1
Напишіть запит, який поверне список унікальних ідентифікаторів виробників.
Враховуйте тільки замовлення з парною сумою замовлення і непарною кількістю замовлених одиниць товару.
- Використовується таблиця [dbo].[ORDERS]
- Результативний набір даних містить: ідентифікатор виробника
- Відсортувати рез. набір даних за ідентифікатором виробника
*/
SELECT DISTINCT [MFR]
FROM [dbo].[ORDERS]
WHERE [AMOUNT] % 2 = 0
  AND [QTY]% 2 != 0
GROUP BY [MFR]
ORDER BY [MFR];
/*
Задача 2
Напишіть запит, який у розрізі ідентифікатора виробника товару поверне загальну вартість од. товару на складі 
та середню вартість од. товарів. Щоб розрахувати вартість товару на складі, необхідно помножити кількість од. 
товару на ціну за одиницю.
Округлити результати агрегування до 2 знаків після коми.
- Використовується таблиця [dbo].[PRODUCTS]
- Результуючий набір даних містить: ідентифікатор виробника, загальну суму, середню суму.
- Відсортувати результуючий набір даних за ідентифікатором виробника
*/
SELECT
      [MFR_ID],
	  SUM ([PRICE]*[QTY_ON_HAND]) AS "Загальна вартість товару",
	  ROUND (AVG([PRICE]), 2) AS "Середня ціна"
FROM [dbo].[PRODUCTS]
GROUP BY [MFR_ID]
ORDER BY [MFR_ID];
/*
Задача 3
Напишіть запит, який поверне працівника з найбільшою мат. різницею між ціллю за продажами і сумою поточних продаж, 
у абсолютному вимірі, тобто необхідно взяти число за модулем. Округлити результати обчислення до двох знаків після 
коми. Розрахувати кількість років працівника на момент найму.
Враховуйте ймовірність того, що відразу кілька працівників можуть мати одну і ту ж різницю.
- Використовується таблиця [dbo].[SALESREPS]
*/
SELECT TOP 1 WITH TIES
                  [EMPL_NUM],
				  [NAME],
				  [AGE],
				  [REP_OFFICE],
				  [TITLE],
				  [HIRE_DATE],
				  [MANAGER],
				  ROUND (ABS([QUOTA]-[SALES]), 2) AS "Різниця між ціллю за продажами і сумою поточних продаж"
FROM [dbo].[SALESREPS]
GROUP BY 
        [EMPL_NUM],
		[NAME],
	    [AGE],
		[REP_OFFICE],
		[TITLE],
		[HIRE_DATE],
		[MANAGER],
		ROUND (ABS([QUOTA]-[SALES]), 2)
ORDER BY "Різниця між ціллю за продажами і сумою поточних продаж" DESC;